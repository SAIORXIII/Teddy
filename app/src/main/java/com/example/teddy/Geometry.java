package com.example.teddy;

public class Geometry {

    private long x;
    private long y;

    public Geometry(long x, long y) {
        this.x = x;
        this.y = y;
    }

    public Geometry(String x, String y){}


    public long getX() {
        return x;
    }

    public void setX(long x) {
        this.x = x;
    }

    public long getY() {
        return y;
    }

    public void setY(long y) {
        this.y = y;
    }





}

